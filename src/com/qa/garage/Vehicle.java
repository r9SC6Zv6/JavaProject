package com.qa.garage;

public class Vehicle {
	int id;
	int noOfWeels;
	String size;
	String colour;
	int maxSpeed;

	public Vehicle(int id, int noOfWeels, String size, String colour, int maxSpeed) {
		super();
		this.id = id;
		this.noOfWeels = noOfWeels;
		this.size = size;
		this.colour = colour;
		this.maxSpeed = maxSpeed;
	}

}
